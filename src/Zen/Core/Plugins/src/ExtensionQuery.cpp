{
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
ExtensionQuery::~ExtensionQuery()
{
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
void
ExtensionQuery::setNamespace(const std::string& _namespace)
{
    m_namespace = _namespace;

    convertToString();
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
void
ExtensionQuery::setExtensionPoint(const std::string& _extensionPoint)
{
    m_extensionPoint = _extensionPoint;

    convertToString();
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
void
ExtensionQuery::setType(const std::string& _type)
{
    m_type = _type;

    convertToString();
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
const std::string&
ExtensionQuery::toString() const
{
    return m_string;
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
const std::string& 
ExtensionQuery::getNamespace() const
{
    return m_namespace;
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
const std::string& 
ExtensionQuery::getExtensionPoint() const
{
    return m_extensionPoint;
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
const std::string& 
ExtensionQuery::getType() const
{
    return m_type;
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
void
ExtensionQuery::convertToString()
{
    // TODO This implementation is extremely innefficient and should be improved.

    std::stringstream stringValue;
    stringValue << m_namespace << "::" << m_extensionPoint  << "[type = " << m_type << "]";
    m_string = stringValue.str();
}

